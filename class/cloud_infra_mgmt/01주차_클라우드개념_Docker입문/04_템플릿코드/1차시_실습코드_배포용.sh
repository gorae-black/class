#!/usr/bin/env bash
# ============================================================================
# 클라우드인프라관리 1주차 1차시 실습코드 [배포용 · 자기주도 학습]
# 대상: Docker를 처음 접하는 초보자
# 사용법: 아래 명령어를 한 줄씩 순서대로 복사해서 터미널에 붙여넣고 Enter를
#         누르세요. 각 명령어 위 주석(#으로 시작하는 줄)은 실행하지 않아도
#         되는 설명입니다.
# 자세한 화면 캡처와 단계별 설명은 03_실습가이드_PDF 폴더의
# "1차시_실습가이드.pdf" 문서를 함께 참고하세요.
# ============================================================================


# ============================================================
# PART A. Killercoda 브라우저 실습 환경에서 진행
# (LMS > 1주차 > 1차시 > 05_Killercoda_링크 의 URL로 접속 후 진행)
# ============================================================

# --------------------------------------------------------------
# [1] 첫 번째 컨테이너 실행하기
# docker run = "이미지를 실행해서 컨테이너로 만들어라"는 명령어입니다.
# hello-world 이미지가 로컬(Killercoda 환경)에 없으면 자동으로
# Docker Hub에서 내려받은(pull) 뒤 실행합니다.
# --------------------------------------------------------------
docker run hello-world

# 실행 결과 예시:
#   Unable to find image 'hello-world:latest' locally
#   latest: Pulling from library/hello-world
#   ...
#   Hello from Docker!
#   This message shows that your installation appears to be working correctly.
#
# "Hello from Docker!" 메시지가 보이면 성공입니다.


# --------------------------------------------------------------
# [2] 내가 가진 이미지 목록 확인하기
# docker images = 현재 로컬에 저장된(다운로드된) 이미지 목록을 보여줍니다.
# 이미지는 컨테이너를 만들기 위한 "설계도(붕어빵 틀)"라는 것을 기억하세요.
# --------------------------------------------------------------
docker images

# 실행 결과 예시:
#   REPOSITORY    TAG       IMAGE ID       CREATED         SIZE
#   hello-world   latest    d2c94e258dcb   3 weeks ago     13.3kB
#
# SIZE가 13.3kB밖에 안 되는 것에 주목하세요 — 컨테이너 이미지가
# 왜 가상머신(VM)보다 훨씬 가벼운지 직접 확인한 것입니다.


# --------------------------------------------------------------
# [3] 내가 실행한 컨테이너 목록 확인하기 (전체)
# docker ps = 현재 "실행 중인" 컨테이너만 보여줍니다.
# docker ps -a = 실행이 끝나 "종료된" 컨테이너까지 전부(all) 보여줍니다.
# --------------------------------------------------------------
docker ps -a

# 실행 결과 예시:
#   CONTAINER ID   IMAGE         COMMAND    CREATED        STATUS                     NAMES
#   3f1a2b9c8d7e   hello-world   "/hello"   1 minute ago   Exited (0) 1 minute ago    festive_lovelace
#
# STATUS가 "Exited (0)"인 것은 정상입니다. hello-world는 인사 메시지를
# 출력하고 할 일을 마쳤기 때문에 자동으로 종료된 것이며 오류가 아닙니다.


# --------------------------------------------------------------
# [심화, 선택] 조금 더 살펴보고 싶다면 아래 명령어도 실행해 보세요.
# --------------------------------------------------------------
docker --version        # 현재 Docker 버전 확인
docker info             # Docker 엔진의 상세 정보(컨테이너/이미지 개수 등) 확인
docker run hello-world  # 다시 한 번 실행해보면 "Pulling"과정 없이 바로 실행됨
                         # → 이미 로컬에 이미지가 있기 때문 (다운로드 생략)


# ============================================================
# PART B. 본인 PC 터미널(로컬)에서 진행 — 0주차 VM 최종 점검
# Windows: PowerShell 또는 Windows Terminal
# Mac: 터미널(Terminal) 앱
# ============================================================

# --------------------------------------------------------------
# [4] 0주차에 만든 개인 실습용 VM에 접속하기
# multipass shell <VM이름> = 지정한 이름의 VM 내부로 들어갑니다.
# 접속에 성공하면 프롬프트가 ubuntu@cloud-lab:~$ 형태로 바뀝니다.
# --------------------------------------------------------------
multipass shell cloud-lab

# 만약 "command not found: multipass" 오류가 뜬다면:
#   → Multipass가 설치되지 않은 것입니다. 0주차 가이드의 설치 스크립트를
#     다시 실행한 뒤 터미널을 재시작하세요.
#   Windows: winget install Canonical.Multipass
#   Mac    : brew install multipass


# --------------------------------------------------------------
# [5] VM 내부에서 Docker / Git 정상 설치 여부 확인
# 아래 두 명령어는 반드시 VM 접속 후(프롬프트가 cloud-lab으로 바뀐 뒤)
# 실행해야 합니다.
# --------------------------------------------------------------
docker --version
# 예시 출력: Docker version 24.0.x, build xxxxxxx

git --version
# 예시 출력: git version 2.43.x

# 두 명령어 모두 버전 번호가 정상 출력되면 0주차 VM 환경 구성이
# 완료된 것입니다. 앞으로 매주 이 VM에서 심화 과제를 진행합니다.


# --------------------------------------------------------------
# [6] (선택) VM 접속 종료하기
# exit 명령어를 입력하면 VM에서 빠져나와 내 PC의 터미널로 돌아갑니다.
# VM 자체가 꺼지는 것은 아니며, 다시 [4]번 명령어로 언제든 재접속할 수
# 있습니다.
# --------------------------------------------------------------
exit


